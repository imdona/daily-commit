# case 2 : 메모리 32976KB / 시간 68ms(시간 case1와 동일)
import math
import sys
N = int(sys.stdin.readline())

n_factorial = str(math.factorial(N))
# print(n_factorial)  # 3628800
count = 0

#  1의 자리부터 처음 0이 아닌 숫자가 나오기까지 0의 개수 카운트
# str의 맨뒤에서부터 시작 ~ 처음까지, 거꾸로(-1)
for i in range(len(n_factorial)-1, -1, -1):
    # print(f"i : {i}")  # i: 6, 5, 4 break!
    if n_factorial[i] == '0':
        count += 1
    else:
        break
print(count)
